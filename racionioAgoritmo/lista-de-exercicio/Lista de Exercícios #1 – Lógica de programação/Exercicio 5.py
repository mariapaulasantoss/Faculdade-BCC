#Entrada da distância total percorrida (KM)
distanciaPercorrida = float(input('Entre com a distância percorrida em Km:' ))
#Entrada do volume de combustível consumido em L
combustivel= float(input('Entre com o combustivel consumido em litros:'))

#Calculo do consumo médio
consumoMedio = distanciaPercorrida/combustivel

#Resultado
print('Consumo médio foi:', consumoMedio)