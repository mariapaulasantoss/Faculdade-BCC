#Faça um algoritmo que leia um número inteiro e escreva seu antecessor e sucesso.

#Entrada do numero inteiro
numeroIntereiro = int(input("Entrada do numero inteiro"))

#Calculo do sucessor e antecessor
sucessor= numeroIntereiro + 1
antecessor = numeroIntereiro - 1

#Retorno
print('sucessor: ', sucessor)
print('antecessor:', antecessor)
